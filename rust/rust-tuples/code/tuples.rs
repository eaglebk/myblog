fn main() {
    // Пример кортежа с разными типами
    let t1: (u32, u8, f64) = (500, 1, 6.28);

    // Пример кортежа с одинаковыми типами
    let t2: (u32, u32, u32) = (10, 20, 30);

    // Пример вложенного кортежа
    let t3: (u32, (u64, u8), u32) = (100, (50, 2), 200);

    println!("t1: {:?}", t1);
    println!("t2: {:?}", t2);
    println!("t3: {:?}", t3);
}
// ! **Объявление кортежей в Rust**
// ! - Кортеж создается путем перечисления значений через запятую в круглых скобках `()`.
// ! - Типы элементов могут полностью отличаться (`t1`) или быть одинаковыми (`t2`).
// ! - Кортежи могут содержать другие кортежи в качестве элементов (`t3`), формируя вложенные структуры.

// ---

fn main() {
    let x: (i32, f64, u8) = (500, 6.4, 1);

    // Доступ к элементам по порядковому номеру (индексу)
    let five_hundred = x.0;
    let six_point_four = x.1;
    let one = x.2;

    println!("Элементы: {}, {}, {}", five_hundred, six_point_four, one);

    // Деструктуризация кортежа
    let (a, b, c) = x;
    println!("Деструктуризация: a = {}, b = {}, c = {}", a, b, c);
}
// ! **Доступ к элементам и деструктуризация**
// ! - Доступ к конкретным элементам кортежа осуществляется через «точку» и его индекс: `x.0`, `x.1`, `x.2`.
// ! - Индексация начинается с нуля.
// ! - Кортеж также можно разобрать на отдельные переменные с помощью деструктуризации: `let (a, b, c) = x;`.

// ---

// ?hidden:start
#[allow(unused_variables)]
// ?hidden:end
fn main() {
    // Пример блока кода, вычисляющегося в единичный тип ():
    let x: () = {
        println!("Печать внутри блока кода");
        // Выражение с точкой с запятой в конце возвращает ()
    };

    // Функция, не возвращающая значения, возвращает ()
    let y: () = implicit_unit();

    println!("x: {:?}", x);
    println!("y: {:?}", y);
}

// Функция неявно возвращает ()
fn implicit_unit() {
    // Нет выражения возврата
}
// ! **Единичный тип (Unit-type)**
// ! - Пустой кортеж `()` представляет собой единичный тип.
// ! - Блоки кода, заканчивающиеся точкой с запятой `;`, вычисляются в `()`.
// ! - Функции, не имеющие явного возвращаемого типа, неявно возвращают `()`.
