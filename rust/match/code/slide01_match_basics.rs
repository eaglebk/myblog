fn main() {
    let season = "лето";

    match season {
        "весна"  => println!("Весна — всё цветёт"),
        "лето"   => println!("Лето — самое тёплое время"),
        "осень"  => println!("Осень — время листопада"),
        "зима"   => println!("Зима — время снега"),
        _        => println!("Неизвестное время года"),
    }
}
// ! **Базовый синтаксис match**
// ! - Конструкция `match` сравнивает значение с каждой «веткой» по порядку и выполняет код первого совпадения.
// ! - Паттерн `_` — специальный «заглушитель», который срабатывает, если ни один другой шаблон не совпал. Rust требует, чтобы все возможные значения были обработаны.

// ---

fn main() {
    let pair = (0, -2);

    match pair {
        (first, second) if first == 0 => println!("Первый — 0, второй: {}", second),
        (first, second) if second == 0 => println!("Первый: {}, второй — 0", first),
        _                              => println!("Другая комбинация"),
    }
}
// ! **Match с деструктуризацией кортежей**
// ! - `match` умеет разбирать (деструктурировать) сложные типы данных, например кортежи.
// ! - Здесь `(first, second) if first == 0` совпадает с любым кортежем, где первый элемент равен `0`, и связывает оба элемента с переменными.

// ---

fn main() {
    let pair = (4, 7);

    match pair {
        (x, y) if x == y     => println!("Числа одинаковые: {} и {}", x, y),
        (x, y) if x + y == 0 => println!("{} и {} — противоположны (сумма = 0)", x, y),
        (x, _) if x % 2 == 0 => println!("Первое число {} — чётное", x),
        _                    => println!("Нет особой связи"),
    }
}
// ! **Match с условиями (Match Guards)**
// ! - После шаблона можно дополнительно указать условие с помощью ключевого слова `if`.
// ! - Ветка срабатывает только если шаблон совпал **и** условие выполняется.
// ! - Это позволяет точно и лаконично фильтровать конкретные сценарии.
