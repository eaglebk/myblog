// ?hidden:start
use std::sync::mpsc;
use std::thread;
use std::time::Duration;
// ?hidden:end

fn main() {
    let (tx, rx) = mpsc::channel();

    // Запускаем поток, имитирующий долгую работу навигатора
    thread::spawn(move || {
        println!("[НАВИГАЦИЯ] Выполняем расчет траектории...");
        thread::sleep(Duration::from_millis(500));
        let _ = tx.send("Траектория рассчитана!".to_string());
    });

    println!("[МОСТИК] Ждем данные от навигатора...");
    
    // Внимание: recv() БЛОКИРУЕТ текущий поток!
    // Если этот поток управляет всем интерфейсом или рантаймом,
    // вся программа зависнет в ожидании.
    match rx.recv() {
        Ok(msg) => println!("[МОСТИК] Успех: {}", msg),
        Err(e) => println!("[МОСТИК] Ошибка связи: {:?}", e),
    }

    println!("[МОСТИК] Работа продолжается.");
}

// ! **Синхронный блокирующий канал std::sync::mpsc**
// ! - Вызов `rx.recv()` полностью останавливает текущий поток ОС до тех пор, пока не придут данные.
// ! - В асинхронном окружении (например, Tokio) блокировка потока воркера парализует выполнение всех остальных задач на этом потоке.
