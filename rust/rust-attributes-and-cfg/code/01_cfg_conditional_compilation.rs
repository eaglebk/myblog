// Шаг 1: Условная компиляция функций с помощью атрибута #[cfg]

// Данная функция скомпилируется только на операционной системе Linux
#[cfg(target_os = "linux")]
fn get_os_family() -> &'static str {
    "Linux OS"
}

// Данная функция скомпилируется только на операционной системе macOS (darwin)
#[cfg(target_os = "macos")]
fn get_os_family() -> &'static str {
    "macOS (Darwin)"
}

// Данная функция скомпилируется только на операционной системе Windows
#[cfg(target_os = "windows")]
fn get_os_family() -> &'static str {
    "Windows OS"
}

// Резервная реализация для всех остальных систем
#[cfg(not(any(target_os = "linux", target_os = "macos", target_os = "windows")))]
fn get_os_family() -> &'static str {
    "Другая операционная система"
}

fn main() {
    println!("Текущая операционная система: {}", get_os_family());
}

// ! **1. Условная компиляция #[cfg]**
// ! - Атрибут `#[cfg(...)]` исключает не удовлетворяющий условию код прямо во время парсинга на этапе компиляции.
// ! - Поддерживает логические операторы `any(...)`, `all(...)` и `not(...)`.

// ---

// Шаг 2: Использование динамического макроса cfg!(...)
fn main() {
    // В отличие от атрибута #[cfg], макрос cfg! возвращает bool во время компиляции
    if cfg!(target_pointer_width = "64") {
        println!("Программа скомпилирована под 64-битную архитектуру");
    } else {
        println!("Программа скомпилирована под 32-битную архитектуру");
    }

    if cfg!(debug_assertions) {
        println!("Режим сборки: Debug");
    } else {
        println!("Режим сборки: Release (оптимизированный)");
    }
}

// ! **2. Макрос cfg!(...)**
// ! - Макрос `cfg!(...)` оставляет код скомпилированным, но вычисляет булево значение `true`/`false`.
// ! - Позволяет ветвить логику внутри обычных `if`-выражений.
