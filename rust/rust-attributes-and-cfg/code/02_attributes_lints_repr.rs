// Шаг 1: Управление линтером и оптимизациями (#[allow], #[inline])

// Подавляем предупреждения о неиспользуемом коде
#[allow(dead_code)]
struct UnusedConfig {
    debug_level: u32,
}

// Принудительное встраивание тела функции компилятором
#[inline(always)]
fn fast_add(a: i32, b: i32) -> i32 {
    a + b
}

fn main() {
    let result = fast_add(10, 20);
    println!("Результат быстрого сложения: {result}");
}

// ! **1. Атрибуты lints и inline**
// ! - `#[allow(...)]` подавляет выбранные предупреждения компилятора.
// ! - `#[inline]` и `#[inline(always)]` подсказывают компилятору встроить тело функции в точку вызова для ускорения выполнения.

// ---

// Шаг 2: Разметка памяти C-ABI через атрибут #[repr(C)]
#[repr(C)]
struct PointC {
    x: f64,
    y: f64,
}

fn main() {
    let point = PointC { x: 3.0, y: 4.0 };
    println!("Размер структуры с C-ABI разметкой: {} байт", std::mem::size_of::<PointC>());
    println!("Координаты: ({}, {})", point.x, point.y);
}

// ! **2. Атрибут C-ABI разметки #[repr(C)]**
// ! - По умолчанию Rust свободно переупорядочивает поля структуры для минимизации выравнивания и паддинга.
// ! - `#[repr(C)]` фиксирует порядок и выравнивание полей по стандарту языка C, что критично для FFI и взаимодействия с C-библиотеками.
