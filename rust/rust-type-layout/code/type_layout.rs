use std::mem::{size_of, align_of};

#[allow(dead_code)]
struct Foo {
    a: u8,
    b: u32,
    c: u16,
}

fn main() {
    println!("Размер Foo: {} байт", size_of::<Foo>());
    println!("Выравнивание Foo: {} байт", align_of::<Foo>());
    
    // Если бы поля лежали в памяти строго по порядку (a, b, c):
    // 1 байт (a) + 3 байта паддинга + 4 байта (b) + 2 байта (c) + 2 байта паддинга = 12 байт.
    // Однако компилятор Rust переупорядочил поля в памяти (b, c, a):
    // 4 байта (b) + 2 байта (c) + 1 байт (a) + 1 байт паддинга = 8 байт!
}
// ! **Выравнивание и размер по умолчанию**
// ! - Компилятор Rust автоматически вставляет байты заполнения (padding) для правильного выравнивания.
// ! - По умолчанию Rust свободно меняет порядок полей в памяти для минимизации размера структуры.
// ! - Для `Foo` размер уменьшается с 12 до 8 байт благодаря оптимизации переупорядочивания полей.

// ---

use std::mem::size_of;

#[repr(C)]
#[allow(dead_code)]
struct StructC {
    a: u8,
    b: u32,
    c: u16,
}

#[repr(align(16))]
#[allow(dead_code)]
struct StructAlign {
    a: u32,
}

#[repr(transparent)]
#[allow(dead_code)]
struct Wrapper(u32);

fn main() {
    println!("Размер StructC (repr(C)): {} байт", size_of::<StructC>());
    println!("Размер StructAlign (repr(align(16))): {} байт", size_of::<StructAlign>());
    println!("Размер Wrapper (repr(transparent)): {} байт", size_of::<Wrapper>());
}
// ! **Атрибуты представления #[repr(...)]**
// ! - `#[repr(C)]` выстраивает поля строго по порядку объявления (как в C/C++), запрещая переупорядочивание.
// ! - `#[repr(align(N))]` задает минимальное выравнивание структуры в памяти (в байтах).
// ! - `#[repr(transparent)]` гарантирует, что структура-обертка имеет точно такое же представление, как и ее единственное поле.

// ---

#[repr(u32)] // Задаем тип тега (дискриминанта) в памяти как u32
#[allow(dead_code)]
enum Status {
    Active = 42,
    Inactive = 24,
    Pending = 134,
}

fn main() {
    let val = Status::Active;
    
    // Приведение перечисления к целому числу с помощью оператора `as`
    let num = val as u32;
    
    println!("Тег Status::Active в памяти равен: {}", num);
}
// ! **Дискриминанты перечислений**
// ! - Мы можем явно задавать числовые значения (дискриминанты) вариантам enums.
// ! - Атрибут `#[repr(u32)]` (или другие целочисленные типы) фиксирует тип хранения тега в памяти.
// ! - Варианты можно приводить к соответствующему числу с помощью оператора `as`.
