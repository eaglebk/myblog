// Шаг 1: Создание и разыменование сырых указателей (*const T и *mut T)
fn main() {
    let mut num: i32 = 42;

    // Создание сырых указателей БЕЗОПАСНО и не требует блоков unsafe:
    let ptr_const: *const i32 = &num as *const i32;
    let ptr_mut: *mut i32 = &mut num as *mut i32;

    println!("Адрес памяти (const ptr): {:?}", ptr_const);
    println!("Адрес памяти (mut ptr):   {:?}", ptr_mut);

    // Разыменование и изменение значений требует небезопасного блока unsafe:
    unsafe {
        *ptr_mut = 100;
        println!("Новое значение через *ptr_mut: {}", *ptr_const);
    }
}

// ! **1. Работа с сырыми указателями**
// ! - В отличие от обычных ссылок `&T`, сырые указатели могут быть нулевыми (null), невыровненными и игнорируют правила Borrow Checker.
// ! - Создавать указатели можно в безопасном коде, но их **разыменование (`*ptr`)** строго требует блока `unsafe`.

// ---

// Шаг 2: Создание безопасной обертки (Safe Abstraction) над Unsafe кодом
fn swap_raw_pointers(a: &mut i32, b: &mut i32) {
    // Внутренняя реализация использует сырые указатели,
    // но внешняя функция имеет безопасную сигнатуру для пользователя!
    let raw_a: *mut i32 = a;
    let raw_b: *mut i32 = b;

    unsafe {
        let temp = *raw_a;
        *raw_a = *raw_b;
        *raw_b = temp;
    }
}

fn main() {
    let mut x = 10;
    let mut y = 20;

    println!("До обмена: x = {x}, y = {y}");
    swap_raw_pointers(&mut x, &mut y);
    println!("После обмена: x = {x}, y = {y}");
}

// ! **2. Безопасные абстракции над Unsafe**
// ! - Главный принцип Rust: использовать `unsafe` внутри небольших хорошо изолированных модулей, предоставляя пользователю безопасный интерфейс.
