#[allow(dead_code)]
#[derive(Debug)]
enum WebEvent {
    // Юнит-вариант (без связанных данных)
    PageLoad,
    // Кортежный вариант (со связанными безымянными данными)
    KeyPress(char),
    // Структурный вариант (с именованными полями)
    Click { x: i64, y: i64 },
}

fn main() {
    let load = WebEvent::PageLoad;
    let press = WebEvent::KeyPress('q');
    let click = WebEvent::Click { x: 320, y: 240 };

    println!("load: {:?}", load);
    println!("press: {:?}", press);
    println!("click: {:?}", click);
}
// ! **Варианты конструирования перечислений**
// ! - Перечисления (enums) могут объединять данные разных форматов под одним типом.
// ! - Юнит-варианты не несут связанных данных (`PageLoad`).
// ! - Кортежные варианты хранят неименованные данные в круглых скобках (`KeyPress`).
// ! - Структурные варианты хранят именованные поля в фигурных скобках (`Click`).

// ---

// Определение незаселенного типа (uninhabited type)
// Перечисление без вариантов
#[allow(dead_code)]
enum EmptyEnum {}

fn main() {
    // Экземпляр EmptyEnum создать невозможно, так как нет вариантов!
    println!("EmptyEnum определен, но не может быть инициализирован.");
}
// ! **Незаселенные типы (Uninhabited Types)**
// ! - Перечисление без единого варианта называется незаселенным типом.
// ! - Создать экземпляр такого типа в безопасном коде невозможно.
// ! - Используется в системе типов для обозначения невозможных ситуаций (например, ошибки, которая никогда не произойдет).

// ---

#[allow(dead_code)]
enum Message {
    Quit,
    Move { x: i32, y: i32 },
    Write(String),
}

fn main() {
    let msg = Message::Move { x: 10, y: 20 };

    // Извлечение данных через сопоставление паттернов (pattern matching)
    match msg {
        Message::Quit => {
            println!("Событие выхода");
        }
        Message::Move { x, y } => {
            println!("Перемещение в координаты x = {}, y = {}", x, y);
        }
        Message::Write(text) => {
            println!("Запись сообщения: {}", text);
        }
    }
}
// ! **Извлечение данных с помощью match**
// ! - Механизм `match` гарантирует безопасный доступ к данным внутри вариантов enums.
// ! - Компилятор Rust требует обязательной обработки всех вариантов перечисления (исчерпывающее сопоставление).
// ! - Данные деструктурируются прямо в шаблоне ветки `match`.
