fn main() {
    // В Rust строгая типизация. Мы не можем присвоить число с плавающей точкой
    // переменной, которая ожидает целое число без явного приведения.
    let speed: i32 = 12.5; 
    println!("Скорость: {}", speed);
}
// ! **Ошибка E0308: Несоответствие типов (Mismatched Types)**
// ! - Мы указали тип `i32` (целое число), но присвоили значение `12.5` (с плавающей точкой).
// ! - Rust не выполняет неявных преобразований типов, чтобы уберечь вас от потери данных или точности.

// ---

fn main() {
    // Способ 1: Изменить тип переменной на соответствующий
    let speed_float: f64 = 12.5;
    
    // Способ 2: Явное приведение типов с помощью оператора `as`
    let speed_int: i32 = 12.5 as i32;

    println!("Дробная скорость: {}, Целая скорость: {}", speed_float, speed_int);
}
// ! **Решение несоответствия типов**
// ! - Либо меняем тип на `f64` или `f32` для сохранения точности.
// ! - Либо явно приводим значение к `i32` с помощью `as i32` (при этом дробная часть будет отброшена).
